public class EscapeSequences {

    public static void main(String[] args) {
        System.out.println("Java \nPython \nC#");// her biri ayri satirda yani alt alta olsun diye /n kullanabilirsin

        System.out.println("---------------------------------------");

        System.out.println("Hello Cydeo \nHow are you all today? \nIt`s nice to see you all!");

        System.out.println("----------------------------------------");

        System.out.println("\tJava is Cool Programming Language");

        System.out.println("-----------------------------------------");

        System.out.println("/ \\"); //  "\\" bu sana tek \ bundan verir. iki tane \\ istiyorsan dort tane \\\\ yap

        System.out.println("------------------------------------------");

        System.out.println(" My favorite things \"Cilve\"");
    }


}
