package day05_Concatenation;

public class StringConcatenation {

    /*
    Addition: +
 number +  number ==> addition
 1   +    2   ===> 3

Concatenation: +

String + Data
Data   + String
String + String

Concatenation
•The action of linking things together by using “+” operator
•There must be a string of text on the right or left side of the “+” operator

 */
    public static void main(String[] args) {

        System.out.println("This is " + "one string."); // This is one string.

        int number = 5;
        System.out.println(" The value is " +  number);  //The value is 5
// tirnak isaretinden sonra icinde bsluk birakmak lazim birlestirdigi cumleyi ayirmak icin
    }
}

